char	*ft_strupcase(char *str)
{
	int		i;
	char	c;

	i = 0;
	while (str[i])
	{
		if (str[i] >= 97 && str[i] <= 122)
		{
			c = str[i];
			c -= 32;
			str[i] = c;
		}
		i++;
	}
	return (str);
}
